
##sum of the two variables
def add (a, b):
    return a + b

##product of the variables
def multiply (a, b):
    return a * b

##difference of the variables
def subtract(a, b):
    return a - b

##division of the variables
def divide(a, b):
    if b != 0:
        return a / b
    else:
        return "Division by zero error"